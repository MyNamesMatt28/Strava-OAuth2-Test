export const getParamValues = (url) => {
    return url
        .slice(1)
        .split('&')
        .reduce((prev, curr) => {
            const [title, value] = curr.split('=');
            prev[title] = value;
            return prev;
        }, {});
};

export const cleanUpAuthToken = str => {
	return str
        .split('&')[1]
        .slice(5)
};

